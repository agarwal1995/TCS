    private void updatebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebuttonActionPerformed
        if(name.getText().equals("") || author.getText().equals("") || loc1.getText().equals("") || loc2.getText().equals("") || loc3.getText().equals("") )
        {
            JOptionPane.showMessageDialog(null,"Fill up All the Fields ");
        }
        else
        {
            String sql="Update book_info Set bookname=?,authorname=?,location_1=?,location_2=?,location_3=? where id=?";
            try{
                ps=cn.prepareStatement(sql);
                ps.setString(1,name.getText());
                ps.setString(2,author.getText());
                ps.setString(3,loc1.getText());
                ps.setString(4,loc2.getText());
                ps.setString(5,loc3.getText());
                ps.setString(6,id.getText());
                
                ps.executeUpdate();
                 JOptionPane.showMessageDialog(null,"Book Information Succesfully Updated ");
                 
                 name.setText("");
                 author.setText("");
                 loc1.setText("");
                 loc2.setText("");
                 loc3.setText("");
                
            }
            catch(Exception e)
            {
                 JOptionPane.showMessageDialog(null,e);
            }
            
        }
    }//GEN-LAST:event_updatebuttonActionPerformed

