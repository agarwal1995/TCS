    public void extractinfo()
    {
        if(id.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Enter The Book Id ");
        }
        else
        {
            String sql="Select * from book_info where id=?";
            try{
                ps=cn.prepareStatement(sql);
                ps.setString(1,id.getText());
                rs=ps.executeQuery();
                
                if(rs.next())
                {
                    name.setText(rs.getString("bookname"));
                    author.setText(rs.getString("authorname"));
                    loc1.setText(rs.getString("location_1"));
                    loc2.setText(rs.getString("location_2"));
                    loc3.setText(rs.getString("location_3"));
                    updatebutton.setEnabled(true);
                }
                else
                {
                    JOptionPane.showMessageDialog(null,"Invalid Book Id ");
                }
            }
            catch(Exception e)
            {
                 JOptionPane.showMessageDialog(null,e);
            }
        }
        
    }
